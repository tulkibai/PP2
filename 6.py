#CASTING 

x = int(input("Enter int number pls: "))
print("Casting int to float:", x, "=", float(x))
print("Casting int to str:", x, "=", str(x))

y = float(input("Enter float number pls: "))
print("Casting float to int:", y, "=", int(y))
print("Casting float to str:", y, "=", str(y))

z = str(input("Enter number pls:"))
print(type(z))
print("From string to int:", int(z))
print("From string to float:", float(z))
